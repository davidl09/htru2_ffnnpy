from __future__ import annotations

import argparse
import json
import shutil
import sys
import time
from concurrent.futures import ProcessPoolExecutor, as_completed
from multiprocessing import get_context
from pathlib import Path
from typing import Sequence
from zipfile import ZIP_DEFLATED, ZipFile

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from experiments.htru2_saved_model_stats import default_output_json_path, evaluate_saved_model
from experiments.run_hyperparam_sweep_hpc import available_core_count, format_elapsed, format_progress, log
import read_htru2_arff
from project_paths import resolve_project_path


DEFAULT_MODEL_FILENAME = "model.ffnnpy"
DEFAULT_BUNDLE_ZIP_FILENAME = "htru2_eval_reports.zip"
DEFAULT_VIEWER_FILENAME = "htru2_eval_viewer.html"


def positive_int(value: str) -> int:
    parsed = int(value)
    if parsed < 1:
        raise argparse.ArgumentTypeError("value must be at least 1")
    return parsed


def parse_args(argv: Sequence[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Parallelize experiments/htru2_saved_model_stats.py across all model.ffnnpy "
            "artifacts inside an output directory generated by experiments/run_hyperparam_sweep_hpc.py."
        )
    )
    parser.add_argument(
        "output_dir",
        type=Path,
        help=(
            "Sweep output directory, relative to the repository root or as an absolute "
            "filesystem path."
        ),
    )
    parser.add_argument(
        "--dataset-path",
        type=Path,
        default=read_htru2_arff.DEFAULT_ARFF_PATH,
        help=(
            "Path to the HTRU2 ARFF file, relative to the repository root or as an "
            "absolute filesystem path. Defaults to the bundled dataset."
        ),
    )
    parser.add_argument(
        "--jobs",
        type=positive_int,
        default=available_core_count(),
        help="Number of worker processes to use. Defaults to the available core count.",
    )
    parser.add_argument(
        "--bundle-download",
        action="store_true",
        help=(
            "Copy htru2_eval_viewer.html into the sweep output directory and create "
            "a zip archive of the generated evaluation JSON files for easier download."
        ),
    )
    return parser.parse_args(argv)


def discover_model_paths(output_dir: Path) -> list[Path]:
    if output_dir.is_file():
        if output_dir.name != DEFAULT_MODEL_FILENAME:
            raise ValueError(f"expected a {DEFAULT_MODEL_FILENAME} file: {output_dir}")
        return [output_dir]

    if not output_dir.exists():
        raise FileNotFoundError(output_dir)
    if not output_dir.is_dir():
        raise ValueError(f"expected a directory of model artifacts: {output_dir}")

    return sorted(
        (
            candidate
            for candidate in output_dir.rglob(DEFAULT_MODEL_FILENAME)
            if candidate.is_file()
        ),
        key=lambda path: str(path.relative_to(output_dir)),
    )


def write_model_stats(model_path: Path, dataset_path: Path) -> Path:
    stats = evaluate_saved_model(
        model_path=model_path,
        dataset_path=dataset_path,
        runtime_name="saved",
    )
    output_json_path = default_output_json_path(model_path)
    output_json_path.parent.mkdir(parents=True, exist_ok=True)
    output_json_path.write_text(json.dumps(stats, indent=2) + "\n", encoding="utf-8")
    return output_json_path


def write_model_stats_in_worker(model_path: str, dataset_path: str) -> str:
    return str(write_model_stats(Path(model_path), Path(dataset_path)))


def viewer_source_path() -> Path:
    return resolve_project_path(Path("results") / DEFAULT_VIEWER_FILENAME)


def bundle_root_path(output_dir: Path) -> Path:
    return output_dir if output_dir.is_dir() else output_dir.parent


def default_bundle_zip_path(output_dir: Path) -> Path:
    return bundle_root_path(output_dir) / DEFAULT_BUNDLE_ZIP_FILENAME


def default_bundle_viewer_path(output_dir: Path) -> Path:
    return bundle_root_path(output_dir) / DEFAULT_VIEWER_FILENAME


def bundle_stats_download(
    *,
    output_dir: Path,
    stats_paths: Sequence[Path],
) -> tuple[Path, Path]:
    if not stats_paths:
        raise ValueError("cannot create a bundle without stats JSON files")

    viewer_path = viewer_source_path()
    if not viewer_path.exists():
        raise FileNotFoundError(f"viewer HTML not found: {viewer_path}")

    bundle_root = bundle_root_path(output_dir)
    bundle_root.mkdir(parents=True, exist_ok=True)
    copied_viewer_path = default_bundle_viewer_path(output_dir)
    shutil.copyfile(viewer_path, copied_viewer_path)

    zip_path = default_bundle_zip_path(output_dir)
    with ZipFile(zip_path, "w", compression=ZIP_DEFLATED) as archive:
        archive.write(copied_viewer_path, arcname=DEFAULT_VIEWER_FILENAME)
        for stats_path in sorted(stats_paths):
            if output_dir.is_dir():
                arcname = str(stats_path.relative_to(output_dir))
            else:
                arcname = stats_path.name
            archive.write(stats_path, arcname=arcname)

    return copied_viewer_path, zip_path


def run_stats_over_output_dir(
    *,
    output_dir: Path,
    dataset_path: Path,
    jobs: int,
    bundle_download: bool = False,
) -> list[Path]:
    model_paths = discover_model_paths(output_dir)
    if not model_paths:
        raise ValueError(f"no {DEFAULT_MODEL_FILENAME} artifacts found under {output_dir}")
    if jobs < 1:
        raise ValueError("jobs must be at least 1")

    effective_jobs = max(1, min(jobs, len(model_paths)))
    backend = "single-process" if effective_jobs == 1 else "multiprocessing"
    started = time.perf_counter()
    log(
        "[setup] "
        f"backend={backend} "
        f"jobs={effective_jobs} "
        f"models={len(model_paths)} "
        f"output_dir={output_dir}"
    )

    stats_paths: list[Path] = []
    if effective_jobs == 1:
        for completed, model_path in enumerate(model_paths, start=1):
            stats_path = write_model_stats(model_path, dataset_path)
            stats_paths.append(stats_path)
            log(
                "[stats] "
                f"backend={backend} "
                f"progress={format_progress(completed, len(model_paths))} "
                f"model={model_path} "
                f"output={stats_path}"
            )
    else:
        executor = ProcessPoolExecutor(
            max_workers=effective_jobs,
            mp_context=get_context("spawn"),
        )
        try:
            future_to_model_path = {
                executor.submit(write_model_stats_in_worker, str(model_path), str(dataset_path)): model_path
                for model_path in model_paths
            }
            for completed, future in enumerate(as_completed(future_to_model_path), start=1):
                stats_path = Path(future.result())
                model_path = future_to_model_path[future]
                stats_paths.append(stats_path)
                log(
                    "[stats] "
                    f"backend={backend} "
                    f"progress={format_progress(completed, len(model_paths))} "
                    f"model={model_path} "
                    f"output={stats_path}"
                )
        except Exception:
            executor.shutdown(wait=False, cancel_futures=True)
            raise
        else:
            executor.shutdown(wait=True, cancel_futures=False)

    bundle_elapsed = None
    copied_viewer_path = None
    zip_path = None
    if bundle_download:
        bundle_started = time.perf_counter()
        copied_viewer_path, zip_path = bundle_stats_download(
            output_dir=output_dir,
            stats_paths=stats_paths,
        )
        bundle_elapsed = time.perf_counter() - bundle_started
        log(
            "[bundle] "
            f"viewer={copied_viewer_path} "
            f"zip={zip_path} "
            f"files={len(stats_paths)} "
            f"elapsed={format_elapsed(bundle_elapsed)}"
        )

    elapsed = time.perf_counter() - started
    summary_parts = [
        "[summary]",
        f"backend={backend}",
        f"jobs={effective_jobs}",
        f"models={len(model_paths)}",
        f"stats={len(stats_paths)}",
        f"elapsed={format_elapsed(elapsed)}",
        f"output_dir={output_dir}",
    ]
    if bundle_download:
        summary_parts.append("bundle=created")
        summary_parts.append(f"bundle_elapsed={format_elapsed(bundle_elapsed or 0.0)}")
        summary_parts.append(f"bundle_zip={zip_path}")
    log(" ".join(summary_parts))
    return sorted(stats_paths)


def main(argv: Sequence[str] | None = None) -> None:
    args = parse_args(argv)
    output_dir = resolve_project_path(args.output_dir)
    dataset_path = resolve_project_path(args.dataset_path)
    run_stats_over_output_dir(
        output_dir=output_dir,
        dataset_path=dataset_path,
        jobs=args.jobs,
        bundle_download=args.bundle_download,
    )


if __name__ == "__main__":
    main()
